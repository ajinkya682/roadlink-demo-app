import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Load environment variables from .env
dotenv.config({ path: path.resolve(__dirname, '../.env') });

const config = {
  apiKey: process.env.VITE_FIREBASE_API_KEY || "YOUR_API_KEY",
  projectId: process.env.VITE_FIREBASE_PROJECT_ID || "YOUR_PROJECT_ID",
  messagingSenderId: process.env.VITE_FIREBASE_SENDER_ID || "YOUR_SENDER_ID",
  appId: process.env.VITE_FIREBASE_APP_ID || "YOUR_APP_ID"
};

const outputContent = `// Auto-generated by scripts/generate-sw-config.js
const firebaseConfig = ${JSON.stringify(config, null, 2)};
`;

const outputPath = path.resolve(__dirname, '../public/firebase-config.js');
fs.writeFileSync(outputPath, outputContent);

console.log('Successfully generated public/firebase-config.js');
